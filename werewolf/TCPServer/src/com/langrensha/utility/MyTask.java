package com.langrensha.utility;

import java.util.TimerTask;

public class MyTask extends TimerTask {
	private int count;

	public int getCount() {
		return count;
	}

	public MyTask(int count) {
		this.count = count;
	}

	public void run() {
		System.out.println(count);
		count--;
	}

}
